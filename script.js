var x= 5;
var y= 7;
//adition
console.log('x+y=', x+y);
//sub
console.log('x-y=', x-y);
//Mul
console.log('x*y=', x*y);
//Div
console.log('x/y=', x/y);
//Remainder
console.log('x%y=', x%y);
//inc
console.log('++x=', ++x);
console.log('x++=', x++);
console.log('x=', x);
//Dec
console.log('--x=', --x);
console.log('x--=', x--);
console.log('x=', x);
